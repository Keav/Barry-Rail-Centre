                        <h2>Survey Work for New Turntable Begins at Barry Rail Centre</h2>
                        
                        <img src="images/news1.jpg" alt="Survey Work for New Turntable Begins at Barry Rail Centre"/>
                   
 <p>Surveying work has been undertaken at the Barry Rail Centre for an exciting project that will see the installation of a new locomotive turntable at the Centre.  It is hoped that the turntable will be operational in 2014. </p>

 <p class="indent">The turntable project team is headed up by Chartered Civil Engineers John Buxton, the Managing Director of the Barry Rail Centre, and Ric Auger, both of whom played key roles in the installation of the highly successful turntable at Minehead on the West Somerset Railway. The turntable there had become a focal point for visitors to Minehead and the plan is to emulate this at Barry.</p>
 
<p class="indent">John Buxton, the Chairman of the Cambrian Group that now operates the Barry Rail Centre, said "We are delighted to have begun the process of installing this 1938 built turntable that was originally installed at Bricklayers Arms depot in South London. Once it is in place, we will be able to offer servicing and turning facilities for steam hauled charter trains from London, the Midlands and Bristol which has the potential to bring up to 600 tourists to Barry in each train". </p>
 
<p class="indent">The turntable, constructed by Cowans Sheldon, was originally saved by the Mid-Hants Railway in the 1970's, but was never installed on the line as no suitable site could be found. Cambrian acquired the turntable from the Mid-Hants Railway last year.</p>
 
<p class="indent">Cambrian took over the custodianship of the Barry Rail Centre in January 2011 on a 20 year lease from the Vale of Glamorgan Council.</p>
 
<p class="indent">John Buxton, added "Since taking control of the site we have been making steady improvements, operating passenger services, opening a new caf&eacute; as part of our Barry Tourist Railway brand, and working toward infrastructural improvements to the two miles of running line.  The turntable was always a key part of our business plan, adding not only a useful addition to our own operations, but also, allied to our existing mainline connection, transforming Barry into a railway centre of national importance."</p>
 
<p class="indent">The survey work undertaken has identified that a site at the rear of Barry Rail Centre's New Works Building is most suitable. The location is situated conveniently close to Barry Rail Centre's mainline connection.  The scheme is now being developed to detailed design stage.</p>

                    
                    
                    
